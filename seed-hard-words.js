async function seed() {
  await initDb();
  let added = 0;
  for (const w of hardWords) {
    const [word, phonetic, pos, cn] = w;
    const exists = get('SELECT id FROM words WHERE word = ?', [word]);
    if (!exists) {
      run('INSERT INTO words (word, phonetic, pos, definition_cn, frequency, difficulty) VALUES (?,?,?,?,?,?)',
        [word, phonetic, pos, cn, 3, 4]);
      added++;
    }
  }
  console.log(`✅ 新增 ${added} 个六级词汇`);
  const total = get('SELECT COUNT(*) as c FROM words').c;
  console.log(`📚 总词汇数: ${total}`);
}

seed().catch(e => { console.error('失败:', e); process.exit(1); });
